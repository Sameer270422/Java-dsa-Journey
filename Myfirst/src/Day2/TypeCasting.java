package Day2;
import java.util.*;
public class TypeCasting{
	public static void main(String[] args) {
		int a = 20;
		double b = a; // for Implicit type casting these is no need of using data type
		
		double c = 99.9;
		int d = (int)c; //for explicit type casting we need to use data types.
		final int h = 25; //when final we cannot chnage calues but we can type cast
		double y = h;
		
		System.out.println(b);
		System.out.println(d);
		System.out.println(y);
		
		
		
		
		
		
		
		
	}
}
